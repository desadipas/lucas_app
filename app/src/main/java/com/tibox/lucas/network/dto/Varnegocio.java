public class Varnegocio {
    private int nCodVar;
    private String cNomVar;
    private String cValorVar;
    private int nTipoVar;

    public int getnCodVar() {
        return nCodVar;
    }

    public void setnCodVar(int nCodVar) {
        this.nCodVar = nCodVar;
    }

    public String getcNomVar() {
        return cNomVar;
    }

    public void setcNomVar(String cNomVar) {
        this.cNomVar = cNomVar;
    }

    public String getcValorVar() {
        return cValorVar;
    }

    public void setcValorVar(String cValorVar) {
        this.cValorVar = cValorVar;
    }

    public int getnTipoVar() {
        return nTipoVar;
    }

    public void setnTipoVar(int nTipoVar) {
        this.nTipoVar = nTipoVar;
    }
}
